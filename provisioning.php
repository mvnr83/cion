<p><strong>User Provisioning System</strong> creates, modifies, disables and deletes user accounts across IT infrastructure and business applications. 
Provisioning system use templates and business rules to automate onboarding, off-boarding and other administration workforce processes 
such as, new hires, transfers, promotions and terminations. Typically the system runs on schedule basis and ensures that data is 
transferred or provisioned in the destination system from the source while applying rules/policies that transforms the data for 
destination system.</p>

<p>User provisioning to company software such as enterprise directory, IT-infrastructure commonly known as identity management, has 
typically been a daunting task for enterprises. Automated user provisioning allows tasks that would normally take days to enter mere 
hours, creates a single point of failure, while making your environment more streamlined. Automated user provisioning makes the process 
of user creation, modification and deletion simple, cost effective, secure, efficient and streamlined. </p>

<p>CionSystems provisioning system simplifies this process via their predefined and customizable templates, policies via simple user 
interface. User provisioning doesn't have to be complex anymore.No script or code is needed.
</p>
<center><img src="img/processImg.jpg" class="img-responsive"></center><br>
<br>

<p>CionSystemsProvisioning system has two major components. First one is a service component which is continuously run on schedule basis, 
based on the configuration. Second one is user interface to manage the system which is integrated in Active Directory Manager Application 
under Provisioning Tab.</p><p>&nbsp; </p>

<center><img src="img/component_01.png"></center><p>&nbsp; </p>
<p>Provisioning system has features like import supplement/master data, configuration of transactional filesschema, other configuration settings, 
status tracking, audit logging and error logging.</p>
